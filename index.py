@app.post("/api/analyze")
async def analyze(audio: UploadFile = File(...)):
    # --- validate file type ---
    import pathlib
    ext = pathlib.Path(audio.filename or "audio.wav").suffix.lower()
    if ext not in ALLOWED_SUFFIXES:
        raise HTTPException(
            status_code=400,
            detail=f"Unsupported file type '{ext}'. Use: {', '.join(ALLOWED_SUFFIXES)}",
        )

    audio_bytes = await audio.read()
    if len(audio_bytes) > MAX_AUDIO_BYTES:
        raise HTTPException(
            status_code=413,
            detail=f"File too large ({len(audio_bytes) // 1024 // 1024} MB). Max 25 MB.",
        )

    # --- transcribe ---
    try:
        transcript = await _transcribe(audio_bytes, audio.filename or f"audio{ext}")
    except openai.OpenAIError as e:
        raise HTTPException(status_code=502, detail=f"Whisper API error: {e}")

    if not transcript:
        raise HTTPException(status_code=422, detail="Whisper returned an empty transcript.")

    # --- embed (remote BERT) + linguistic features → XGBoost verdict ---
    features = extract_text_features(transcript)
    embedding = await _embed(clean_text(transcript))
    classification = _classify(embedding, features)
    classification["text_features"] = features

    # --- explain the verdict with Gemini ---
    feedback = _llm_feedback(transcript, classification)

    return {
        "transcript": transcript,
        "word_count": len(transcript.split()),
        "classification": classification,
        "feedback": feedback,
    }
