"""
PitchCoach — FastAPI backend for Vercel deployment.

Everything heavy runs remotely / as pure Python, so the function bundle stays
tiny (no torch, no xgboost, no numpy):
  - Audio transcription → Groq Whisper Large V3            (GROQ_API_KEY)
  - BERT embeddings     → HuggingFace feature-extraction   (HF_API_KEY + HF_MODEL_ID)
  - Classification      → pure-Python XGBoost evaluator     (api/model_predict.py)
  - Coaching feedback   → Google Gemini API                (GOOGLE_API_KEY)

The XGBoost model is shipped as a JSON tree dump (api/model_trees.json) and
evaluated in pure Python — so the good/bad prediction uses BERT embeddings +
linguistic features, and Gemini then explains that prediction.

Set the secrets in your Vercel project dashboard before deploying.
"""

