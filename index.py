sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from gemini_utils import generate_gemini_text
from feature_utils import (
    EMBEDDING_DIM,
    clean_text,
    extract_text_features,
    features_to_vector,
    pool_hf_embedding,
    positive_prob,
)

# Pure-Python XGBoost evaluator (model_predict.py reads model_trees.json, both
# generated by train_xgboost.py). Bundling these instead of the xgboost library
# keeps the serverless function tiny — no xgboost / scipy / numpy at runtime.
sys.path.insert(0, str(Path(__file__).resolve().parent))
try:
    from model_predict import score as _xgb_score
except Exception:
    _xgb_score = None  # surfaced at request time with a clear error

# Frontend HTML is embedded as a Python module so import tracing bundles it
# reliably (data files in /api are not dependably bundled by Vercel).
